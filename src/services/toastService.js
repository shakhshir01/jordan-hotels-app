export const showLoading = (message) => {
  return toast.loading(message, {
    position: 'top-right',
    closeButton: false,
  });
};

export const updateToast = (toastId, options) => {
  toast.update(toastId, {
    ...options,
    isLoading: false,
  });
};

export const dismissToast = (toastId) => {
  toast.dismiss(toastId);
};
